import turtle
turtle.bgcolor("black")
t=turtle.Turtle()
t.pensize(8)
turtle.speed(5)
t.color("red")
t.penup()
t.lt(90)
t.fd(5)
t.pendown()
l = 150
t.rt(45)
t.fd(l)
t.bk(l)
t.lt(90)
t.fd(l)
t.bk(l)
t.rt(45)
t.penup()
t.bk(10)
t.rt(180)
t.pendown()
t.rt(45)
t.fd(l)
t.bk(l)
t.lt(90)
t.fd(l)
t.bk(l)
t.rt(45)
t.penup()
t.fd(150)
t.pendown()
t.write("RIPPLE",'false','center',font=('arabic',20))
t.rt(90)
t.fd(100)
t.rt(180)
t.penup()
t.fd(1000)
turtle.done()